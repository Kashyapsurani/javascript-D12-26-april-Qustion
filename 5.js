var l = 10
for (var x = 1; x <= l; x++) {
  var n = " ";
  for (var y = 1; y <= l; y++) {
    if (x == 1 || y == 1 || x == l || y == l || x == y || x + y == l + 1) {
      n += "*" + " ";
    } else {
      n += "  "
    }
  }
  console.log(n);
}